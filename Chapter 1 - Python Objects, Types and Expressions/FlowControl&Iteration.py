'''
Flow control and iteration:

Python consists of a sequence of statements.


The interpreter executes each statement in order until there are no more statements.
There are two main ways of controlling the flow of program execution, conditional statements
and loops



'''





def conditional_example(x):
    if x == 1:
        print('False')
    elif x == 0:
        print('True')
    else:
        print('Something else')


